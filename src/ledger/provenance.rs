// ─── Private Helpers ──────────────────────────────────────────────────────────

/// Produce a deterministic canonical string for signing.
fn canonical_payload(
    commit_sha: &str,
    description: &str,
    human_weight: f64,
    ai_weight: f64,
    revenue_split: &RevenueSplit,
    reviewer_id: &str,
    timestamp: &str,
) -> String {
    format!(
        "{commit_sha}|{description}|{human_weight}|{ai_weight}|\
         {creator_id}|{creator_pct}|{ledger}|{reviewer_id}|{timestamp}",
        commit_sha = commit_sha,
        description = description,
        human_weight = human_weight,
        ai_weight = ai_weight,
        creator_id = revenue_split.creator_id,
        creator_pct = revenue_split.creator_pct,
        ledger = revenue_split.ledger,
        reviewer_id = reviewer_id,
        timestamp = timestamp,
    )
}

/// Escape a string for safe embedding as a JSON string value (including quotes).
///
/// Escapes: `\`, `"`, `\n`, `\r`, `\t`, and ASCII control characters (U+0000–U+001F).
fn json_escape(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    out.push('"');
    for c in s.chars() {
        match c {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            c if (c as u32) < 0x20 => {
                // Escape remaining control characters as \uXXXX
                out.push_str(&format!("\\u{:04x}", c as u32));
            }
            c => out.push(c),
        }
    }
    out.push('"');
    out
}

/// Portable FNV-1a 256-bit signature (4 × 64-bit segments).
///
/// **Security note**: This is a portable, dependency-free implementation
/// suitable for non-adversarial integrity proofs.  It is NOT cryptographically
/// secure against a determined attacker.  Before deploying provenance trailers
/// in an adversarial context, replace this function with Ed25519 or ML-DSA
/// (FIPS 204 / Dilithium) signing.
fn portable_sign(input: &str) -> String {
    let bytes = input.as_bytes();

    let segment = |seed: u64| -> u64 {
        let mut h: u64 = 0xcbf29ce484222325u64.wrapping_add(seed);
        for &b in bytes {
            h ^= b as u64;
            h = h.wrapping_mul(0x100000001b3);
        }
        h
    };

    let h0 = segment(0);
    let h1 = segment(h0);
    let h2 = segment(h1);
    let h3 = segment(h2);
    format!("{h0:016x}{h1:016x}{h2:016x}{h3:016x}")
}

/// Resolve the SHA of the current HEAD commit via `git rev-parse HEAD`.
fn resolve_head_sha() -> Option<String> {
    std::process::Command::new("git")
        .args(["rev-parse", "HEAD"])
        .output()
        .ok()
        .and_then(|o| {
            if o.status.success() {
                let s = String::from_utf8_lossy(&o.stdout).trim().to_string();
                if s.is_empty() { None } else { Some(s) }
            } else {
                None
            }
        })
}

/// Read an existing git note for `commit_sha`, if any.
fn read_git_note(commit_sha: &str) -> Option<String> {
    std::process::Command::new("git")
        .args(["notes", "show", commit_sha])
        .output()
        .ok()
        .and_then(|o| {
            if o.status.success() {
                let s = String::from_utf8_lossy(&o.stdout).trim().to_string();
                if s.is_empty() { None } else { Some(s) }
            } else {
                None
            }
        })
}

/// ISO 8601 UTC timestamp (seconds since epoch, simplified).
fn current_timestamp() -> String {
    match SystemTime::now().duration_since(UNIX_EPOCH) {
        Ok(d) => format!("{}Z", d.as_secs()),
        Err(_) => "unknownZ".to_string(),
    }
}

